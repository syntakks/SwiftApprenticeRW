import UIKit

/// This func will return the number of characters in a given string.
/// - Parameter word: This is the string that will be measured.
/// - Returns: Int value of the number of characters in the word.
func wordLength(word: String) -> Int {
  return word.count
}

wordLength(word: "Bobby")

